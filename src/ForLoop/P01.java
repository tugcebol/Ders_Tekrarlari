package ForLoop;

public class P01 {
    public static void main(String[] args) {

        // 10 kere "Hello World" yazdıralım, ama her yazışında 1'den 10'a kadar sıra no ile yazdıralım.

        int i;
        for (i=1; i<=10; i++){
            System.out.println(i+"."+"Hello World");
        }


    }
}
