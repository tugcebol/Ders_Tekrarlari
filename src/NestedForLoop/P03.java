package NestedForLoop;

public class P03 {
    public static void main(String[] args) {


        int kisaKenar = 5;
        int uzunKenar = 8;

        for (int i = 1; i <=kisaKenar ; i++) {
            for (int j = 1; j <=uzunKenar ; j++) {
                System.out.print("* ");

            }
            System.out.println("");

        }
    }
}
