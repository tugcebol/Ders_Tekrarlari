package NestedForLoop;

public class P01 {
    public static void main(String[] args) {


        /*

         *
         * *
         * * *
         * * * *
         * * * * *

         */

        int sira =5;

        for (int i = 1; i <=sira ; i++) {
            for (int j = 1; j <=i ; j++) {
                System.out.print("* ");
            }
            System.out.println("");

        }
    }
}
